package Replit;

import java.util.ArrayList;

/*
Create a static method that is called repeatAL
returns nothing
takes in a single parameter - an ArrayList of Booleans

This method should modify its ArrayList parameter by repeating its ArrayList values.

For example, if the parameter is
(true, false, false)
The modified ArrayList should be
(true, false, false, true, false, false)
 */
public class ArrayListRepeatAll {



    public static void repeatAL (ArrayList<Boolean>arrBool1, ArrayList<Boolean>arrBool2, ArrayList<Boolean>arrBool3){

        System.out.print(arrBool1+" "+arrBool2+" "+arrBool3);
        System.out.print(arrBool1+" "+arrBool2+" "+arrBool3);

    }

}
